int	countword(char const *s, char c)
{
	int	count;

	if (!*s)
		return (0);
	count = 0;
	while (*s && *s != '\n')
	{
		while (*s == c)
			s++;
		if (*s)
			count++;
		while (*s != c && *s)
			s++;
	}
	return (count);
}
