int	ft_atoh(char *color)
{
	char	c;
	int		i;
	int		hex;

	hex = 0;
	i = 2;
	while (color[i] != '\0')
	{
		c = color[i++];
		hex *= 16;
		if (c >= '0' && c <= '9')
			hex += c - '0';
		else if (c >= 'A' && c <= 'F')
			hex += c - 'A' + 10;
		else if (c >= 'a' && c <= 'f')
			hex += c - 'a' + 10;
		else
			return (0x6495ED);
	}
	return (hex);
}
